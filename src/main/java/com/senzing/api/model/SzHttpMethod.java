package com.senzing.api.model;

/**
 * Enumerates the various HTTP methods.
 *
 */
public enum SzHttpMethod {
  /**
   * HTTP GET method.
   */
  GET,

  /**
   * HTTP PUT method.
   */
  PUT,

  /**
   * HTTP PATCH method.
   */
  PATCH,

  /**
   * HTTP DELETE method.
   */
  DELETE,

  /**
   * HTTP POST method.
   */
  POST;
}
